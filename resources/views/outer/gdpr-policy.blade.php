                        <p><strong>Rights of Individuals</strong></p>

                        <p>Under the GDPR, individuals have:</p>

                        <p><strong>The right to access</strong> –this means that individuals have the right to request access to their personal data and to ask how their data is used by the company after it has been gathered. We will provide a copy of the personal data, free of charge and in electronic format if requested.</p>

                        <p><strong>The right to be forgotten</strong> – if consumers are no longer customers, or if they withdraw their consent from a company to use their personal data, then they have the right to have their data deleted. We will not use their data in further processing.</p>

                        <p><strong>The right to data portability</strong> – Individuals have a right to transfer their data from one service provider to another. And it must happen in a commonly used and machine-readable format.</p>

                        <p><strong>The right to be informed</strong> – this covers any gathering of data by companies, and individuals must be informed before data is gathered. Consumers have to opt in for their data to be gathered, and consent must be freely given rather than implied.</p>

                        <p><strong>The right to have information corrected</strong> – this ensures that individuals can have their data updated if it is out of date or incomplete or incorrect. We will update the information as informed.</p>

                        <p><strong>The right to restrict processing</strong> – Individuals can request that their data is not used for processing. Their record can remain in place, but not be used. We will not use their data for further processing, if requested.</p>

                        <p><strong>The right to object</strong> – this includes the right of individuals to stop the processing of their data for direct marketing. There are no exemptions to this rule, and any processing must stop as soon as the request is received. In addition, this right must be made clear to individuals at the very start of any communication.</p>

                        <p><strong>The right to be notified</strong> – If there has been a data breach which compromises an individual's personal data, we will inform the individual within 72 hours of first having become aware of the breach.</p>

